package cn.hrms.sysset.entity;

import java.io.Serializable;

/**
 * 公共属性详情
 */
public class PropertyDetails implements Serializable {

	private static final long serialVersionUID = -8107626237168382056L;
	
	/**
	 * 详情编号
	 */
	private Integer id;
	
	/**
	 * 属性编号
	 */
	private Property pro;
	
	/**
	 * 详情名称
	 */
	private String name;
	
	/**
	 * 详情描述
	 */
	private String desc;
	

	/**
	 * 构造函数
	 */
	public PropertyDetails() { }
	public PropertyDetails(Integer id, Property pro, String name, String desc) {
		this.id = id;
		this.pro = pro;
		this.name = name;
		this.desc = desc;
	}
	
	
	/**
	 * 属性封装
	 */
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public Property getPro() {
		return pro;
	}
	public void setPro(Property pro) {
		this.pro = pro;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getDesc() {
		return desc;
	}
	public void setDesc(String desc) {
		this.desc = desc;
	}
	
}
